#QUESTION DESCRIPTION
"""
Write a Python program to check whether a number is even or odd.
"""

#SOLUTION
x=int(input("Enter the number you wish to check whether it is even or not: "))
if (x%2==0):
    print("The number is even")
else:
    print("The number is odd")

#OUTPUT
# Enter the number you wish to check whether it is even or not: 3
# The number is odd
