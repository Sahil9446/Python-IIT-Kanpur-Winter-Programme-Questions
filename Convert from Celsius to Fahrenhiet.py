"""
Write a Python program to convert temperature from Celsius to Fahrenheit
"""

#SOLUTION
a=float(input("Enter the temperature in celcius: "))
b=(a*9/5)+32
print(a,"Celcius is equal to",b,"Fahrenheit")

#OUTPUT
# Enter the temperature in celcius: 24
# 24.0 Celcius is equal to 75.2 Fahrenheit
